public class Student {
    private int id;
    private String name;
    private Aadhar aadhar;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public Aadhar getAadhar() {
        return aadhar;
    }
    public void setAadhar(Aadhar aadhar) {
        this.aadhar = aadhar;
    }
}
